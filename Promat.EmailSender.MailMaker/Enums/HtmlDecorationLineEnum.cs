namespace Promat.EmailSender.MailMaker.Enums;

/// <summary>
/// Determina la decoración de la propiedad css 'text-decoration-line'
/// </summary>
public enum HtmlDecorationLineEnum
{
    /// <summary>
    /// Sin decoración de línea
    /// </summary>
    None,
    /// <summary>
    /// Decoración de línea por debajo del texto
    /// </summary>
    Underline,
    /// <summary>
    /// Decoración de línea por encima del texto
    /// </summary>
    Overline,
    /// <summary>
    /// Decoración de línea tachando del texto
    /// </summary>
    LineThrough
}