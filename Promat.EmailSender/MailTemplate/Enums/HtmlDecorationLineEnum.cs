namespace Promat.EmailSender.MailTemplate.Enums
{
    /// <summary>
    /// Determina la decoración de la propiedad css 'text-decoration-line'
    /// </summary>
    public enum HtmlDecorationLineEnum
    {
        /// <summary>
        /// Sin decoración de linea
        /// </summary>
        None,
        /// <summary>
        /// Decoración de linea por debajo del texto
        /// </summary>
        Underline,
        /// <summary>
        /// Decoración de linea por encima del texto
        /// </summary>
        Overline,
        /// <summary>
        /// Decoración de linea tachando del texto
        /// </summary>
        LineThrough
    }
}